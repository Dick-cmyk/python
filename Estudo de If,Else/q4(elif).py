nota1 = float(input("Quanto você tirou na primeira nota:"))
nota2 = float(input("Quanto você tirou na segunda nota:"))

media = (nota1 + nota2) / 2
print("Sua média foi:",media)

if media >= 6.0:
  print("Você foi aprovado!!")
  
elif media >= 3.0:
  print("Você terá que fazer o Exame!!")

else:
  print("Você foi reprovado!!")










