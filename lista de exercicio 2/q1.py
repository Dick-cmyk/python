dividendo = int(input("Digíte o dividendo (número a ser dividido):"))
divisor = int(input("Digite o divisor (número pelo qual será dividido):"))

quociente = dividendo // divisor
resto = dividendo % divisor
print("Quociente da divisão de",dividendo,"por",divisor,"é:",quociente)
print("Resto da divisão de",dividendo,"por",divisor,"é:",resto)







